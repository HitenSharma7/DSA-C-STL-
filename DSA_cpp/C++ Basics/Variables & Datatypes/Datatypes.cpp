#include <iostream>
using namespace std; 
int main(){
    //Integer Datatype
    int age=58;
    cout<<"My age is "<<age<<endl;

    //Float Datatype
    float pi=3.14;
    cout<<"Value of pi is "<<pi<<endl;

    //Double Datatype
    double gravity=9.8;
    cout<<"Value of gravity is "<<gravity<<endl;

    //Character Datatype
    char grade='KING';
    cout<<"My grade is "<<grade<<endl;

    //Boolean Datatype
    bool isAdult=true;
    cout<<"Am I an adult? "<<isAdult<<endl;

    return 0;
}