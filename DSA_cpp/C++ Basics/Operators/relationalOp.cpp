#include<iostream>
using namespace std;    
int main(){
    int a=6,b=4;
    cout<<(a<b)<<endl; //less than operator
    cout<<(a>b)<<endl; //greater than operator
    cout<<(a<=b)<<endl; //less than or equal to operator
    cout<<(a>=b)<<endl; //greater than or equal to operator
    cout<<(a==b)<<endl; //equal to operator
    cout<<(a!=b)<<endl; //not equal to operator

    //Relational operators are used to compare two values and return a boolean result (true or false) based on the comparison. They are commonly used in conditional statements and loops to control the flow of a program based on certain conditions.
}